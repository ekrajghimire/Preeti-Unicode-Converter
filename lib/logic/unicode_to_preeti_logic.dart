    '३': '3',
    '४': '4',
    '५': '5',
    '६': '6',
    '७': '7',
    '८': '8',
    '९': '9',
    '।': '।',
    '॥': '॥',
  };

  // Replace each Unicode character with its Preeti equivalent
  String converted = text;
  unicodeToPreetiMap.forEach((unicode, preeti) {
    converted = converted.replaceAll(unicode, preeti);
  });
  return converted;
}
